package EXERCICIO.LISTA3.EX1;

public class Carro{
    private int ano;
    private int marca;
    private int modelo;
    private int Id_cliente;
    private int Id_locacao;
    private int valor_dia;

    public Carro(int ano, int marca, int modelo, int Id_cliente, int Id_locacao, int valor_dia){
        this.ano = ano;
        this.marca = marca;
        this.modelo = modelo;
        this.Id_cliente = Id_cliente;
        this.Id_locacao = Id_locacao;
        this.valor_dia = valor_dia;
    }
}
